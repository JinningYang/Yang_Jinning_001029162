    public void populateTable(){
        DefaultTableModel  model=(DefaultTableModel )restaurantJTable.getModel();
        model.setRowCount(0);
        
        
        for(UserAccount userAccount:system.getRestaurantDirectory().getRestaurantList().get(0).getUserAccountDirectory().getUserAccountList()){
            if(userAccount.getRole().toString().equals("RestaurantAdmin")){
                Object[] row=new Object[3];
                row[0]=userAccount.getEmployee().getId();
                row[1]=userAccount.getEmployee().getName();
                row[2] = userAccount.getUsername();
            
                model.addRow(row);
            }
           
        }
    }
    
    private void createJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createJButtonActionPerformed

        String name = nameJTextField1.getText();
        String username = usernameJTextField.getText();
        String password = String.valueOf(passwordJPasswordField.getPassword());
        
        if (!system.usernameAvailable(username)) {
            // BU unique
            // TODO: DUIHUAKUANG here.
            JOptionPane.showMessageDialog(null, "Username Has Been Used! ");
            return;
        }
        if (username== null ||username.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please input the Username!", "CREATE", JOptionPane.ERROR_MESSAGE);
            return;
        }
         if (password== null ||password.isEmpty()){
            JOptionPane.showMessageDialog(null, "Please input the Password!", "CREATE", JOptionPane.ERROR_MESSAGE);
            return;
        }
        Employee employee = system.getEmployeeDirectory().createEmployee(name);
        UserAccount ua = system.getRestaurantDirectory().getRestaurantList().get(0).getUserAccountDirectory().createUserAccount(username, password, employee, new AdminRole(), false);
        RestaurantMana rm = new RestaurantMana(ua, system.getRestaurantDirectory().getRestaurantList().get(0));
        system.getRestaurantDirectory().getRestaurantList().get(0).getUserAccountDirectory().getUserAccountList().add(rm);
        populateTable();

    }//GEN-LAST:event_createJButtonActionPerformed

