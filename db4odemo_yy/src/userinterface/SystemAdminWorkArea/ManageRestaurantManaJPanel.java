    private void deleteJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = restaurantJTable.getSelectedRow();
        if (selectedRow >= 0) {
            int selectionButton = JOptionPane.YES_NO_OPTION;
            int selectionResult = JOptionPane.showConfirmDialog(null, "Are you sure to delete??", "Warning", selectionButton);
            
            if (selectionResult == JOptionPane.YES_OPTION) { 
                Integer userAccountSelectedRow=(Integer) restaurantJTable.getValueAt(selectedRow, 0);
                    for(UserAccount userAccount:system.getRestaurantDirectory().getRestaurantList().get(0).getUserAccountDirectory().getUserAccountList()){
                        if(userAccount.getRole().toString().equals("RestaurantAdmin")&&userAccountSelectedRow.equals(userAccount.getEmployee().getId())){
                            system.getRestaurantDirectory().getRestaurantList().get(0).getUserAccountDirectory().getUserAccountList().remove(userAccount);
                            break;
                        }
                    }
                    populateTable();
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select a Row!!");
        }
        
    }//GEN-LAST:event_deleteJButtonActionPerformed

    private void viewJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = restaurantJTable.getSelectedRow(), col = 0;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select at least a row.", "INFORMATION", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Integer userAccountSelectedRow=(Integer) restaurantJTable.getValueAt(selectedRow, 0);
        UserAccount ua = null;
        for(UserAccount userAccount:system.getRestaurantDirectory().getRestaurantList().get(0).getUserAccountDirectory().getUserAccountList()){
            if(userAccount.getRole().toString().equals("RestaurantAdmin")&&userAccountSelectedRow.equals(userAccount.getEmployee().getId())){
                ua = userAccount;
                break;
            }
        }
        this.userProcessContainer.add("ViewRestaurantManaJPanel",new ViewRestaurantManaJPanel(this.userProcessContainer,this, ua));
        CardLayout cLayout = (CardLayout) this.userProcessContainer.getLayout();
        cLayout.next(userProcessContainer);
        populateTable();
    }//GEN-LAST:event_viewJButtonActionPerformed


