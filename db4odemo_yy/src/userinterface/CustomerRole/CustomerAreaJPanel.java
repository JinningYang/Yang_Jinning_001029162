    public void populateEvaluationTable(){
        DefaultTableModel model = (DefaultTableModel)orderJTable.getModel();
        model.setRowCount(0);
        
        for(Order order:userAccount.getHistoryOrders()){
            
                Object[]row=new Object[5];
                row[0]=order.getOrderId();
                row[1]=order.getOrderMenu().getMenuName();
                row[2]=order.getStatus();

                for(WorkRequest request:restaurant.getWorkQueue().getWorkRequestList()){
                    if(order.getOrderId()==request.getOrder().getOrderId()){
                        row[3]=request.getStatus();
                    }
                }
                
                row[4]=order.getEvaluation();
                model.addRow(row);
            
        }
    }
   
    public void populateMenuInfoTable(){
        DefaultTableModel model=(DefaultTableModel)menuInfoJTable.getModel();
        model.setRowCount(0);
        
        for(Menu menu:menuList){
            Object[]row=new Object[3];
            row[0]=menu.getMenuId();
            row[1]=menu.getMenuName();
            row[2]=menu.getPrice();
            
            model.addRow(row);
        }
    }
    
    public void populateOrderInfoTable(){
        DefaultTableModel model=(DefaultTableModel)orderMenuJTable.getModel();
        model.setRowCount(0);
        
        for(Order order:userAccount.getHistoryOrders()){
            Object[]row=new Object[4];
            row[0]=order.getOrderId();
            row[1]=order.getOrderMenu().getMenuId();
            row[2]=order.getOrderMenu().getMenuName();
            row[3]=order.getOrderMenu().getPrice();
            
            model.addRow(row);
        }
    }
