    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow=orderJTable.getSelectedRow(),col=0;
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select at least a row.", "INFORMATION", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String evaluation=evaluationJTextField.getText();
        Order o = userAccount.getHistoryOrders().get(selectedRow);
        if(o.getStatus()==null){
            JOptionPane.showMessageDialog(this, "This order has not been processed!", "INFORMATION", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(o.getStatus().equals("Decline")){
            JOptionPane.showMessageDialog(this, "Your order has been cancelled", "INFORMATION", JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean found = false;
        for(WorkRequest request:restaurant.getWorkQueue().getWorkRequestList()){
            if(request.getOrder()==o){
                found = true;
                if(!request.getStatus().equals("Completed")) {
                    JOptionPane.showMessageDialog(this, "You haven't received the order yet!", "INFORMATION", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }
        }
        if(!found) {
            JOptionPane.showMessageDialog(this, "You haven't received the order yet!", "INFORMATION", JOptionPane.ERROR_MESSAGE);
            return;
        }

        o.setEvaluation(evaluation);

        if (o.getEvaluation()== null ||o.getEvaluation().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please input the Evaluation!", "CREATE", JOptionPane.ERROR_MESSAGE);
            return;
        }
        populateEvaluationTable();
    }//GEN-LAST:event_submitJButtonActionPerformed

